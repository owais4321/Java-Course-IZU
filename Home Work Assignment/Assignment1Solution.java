class Assignment1Solution{
    public static void main(String[] args) {
        // Task 1: Declare an Integer Variable
        int age = 21;
        System.out.println("Age: " + age);

        // Task 2: Declare and Initialize a Double Variable
        double temperature = 36.6;
        System.out.println("Temperature: " + temperature);

        // Task 3: Boolean Variable Declaration
        boolean isRaining = false;
        System.out.println("Is it raining? " + isRaining);

        // Task 4: Declare and Initialize a Character Variable
        char grade = 'B';
        System.out.println("Grade: " + grade);

        // Task 5: Declare Multiple Variables of the Same Type
        int x = 5, y = 10, z = 15;
        System.out.println("x: " + x + ", y: " + y + ", z: " + z);

        // Task 6: Reassigning Variable Values
        int count = 1;
        System.out.println("Initial count: " + count);
        count = 5;
        System.out.println("Reassigned count (5): " + count);
        count = 10;
        System.out.println("Reassigned count (10): " + count);

        // Task 7: Declare a Constant
        final int MAX_HEIGHT = 250;
        System.out.println("MAX_HEIGHT: " + MAX_HEIGHT);

        // Task 8: Declare a Float Variable
        float pi = 3.14f;
        System.out.println("Pi: " + pi);

        // Task 9: Declare and Print a Long Variable
        long distance = 123456789L;
        System.out.println("Distance: " + distance);

        // Task 10: Declare and Initialize Multiple Boolean Variables
        boolean isDaytime = true;
        boolean isWeekend = false;
        System.out.println("Is it daytime? " + isDaytime);
        System.out.println("Is it weekend? " + isWeekend);
    }
}
